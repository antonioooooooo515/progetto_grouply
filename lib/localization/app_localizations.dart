    final langCode = _localizedValues.containsKey(locale.languageCode)
        ? locale.languageCode
        : 'en';

    String value =
        _localizedValues[langCode]?[key] ?? _localizedValues['en']?[key] ?? key;

    if (params != null) {
      params.forEach((k, v) {
        value = value.replaceAll('{$k}', v);
